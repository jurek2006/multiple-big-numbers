const expect = require("expect");
const { equal, sum, multiply } = require("../bigNumbers");

describe("equal", () => {
    it("should equal numbers' length", () => {
        expect(equal("5", "23415")).toEqual(["00005", "23415"]);
        expect(equal("5546", "23415")).toEqual(["05546", "23415"]);
        expect(equal("55465", "23415")).toEqual(["55465", "23415"]);
        expect(equal("0000000000000000000055465", "1")).toEqual([
            "0000000000000000000055465",
            "0000000000000000000000001"
        ]);
        expect(
            equal(
                "34847320394857539485748974387485764397458783268745864357653",
                "123"
            )
        ).toEqual([
            "34847320394857539485748974387485764397458783268745864357653",
            "00000000000000000000000000000000000000000000000000000000123"
        ]);
        // expect(equal( )).toEqual();
    });
});

describe("sum", () => {
    it("should add numbers as strings", () => {
        expect(sum("5", "23415")).toEqual("23420");
        expect(sum("5546", "23415")).toEqual("28961");
        expect(sum("55465", "23415")).toEqual("78880");
        expect(sum("0000000000000000000055465", "1")).toEqual(
            "0000000000000000000055466"
        );
    });

    it("should add huge numbers as strings (bigger than safe integer)", () => {
        expect(sum("234529007199254740991", "124529007199254740991")).toEqual(
            "359058014398509481982"
        );
        expect(
            sum(
                "234529007199254343454566574345560991",
                "124546457658829534534600719925474453450991"
            )
        ).toEqual("124546692187836733788944174492048799011982");
    });
});

