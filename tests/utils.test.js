const expect = require("expect");
// const { equal, sum, multiply } = require("../bigNumbers");
const { compareResutsChars } = require("../utils/utils");

describe("utils", () => {
    it("should return true when strings (with equaled length) are the same", () => {
        expect(compareResutsChars("123", "123")).toBeTruthy();
        expect(compareResutsChars("00123", "123")).toBeTruthy();
    });
    it("should return false when strings (with equaled length) are not the same", () => {
        expect(compareResutsChars("122", "123")).toEqual("xx2");
        expect(compareResutsChars("00123", "1123")).toEqual("x0xxx");
        expect(compareResutsChars("0123", "1123")).toEqual("0xxx");
        expect(compareResutsChars("12345", "12845")).toEqual("xx3xx");
    });
});
